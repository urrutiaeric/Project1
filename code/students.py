class Students:
    def __init__(self, name, idnum, courses, gpa):
        self.name = name
        self.idnum = idnum
        self.courses = courses
        self.gpa = gpa
        

    def getname(name):
        return name
    
    def getidnum(idnum):
        return idnum
    
    def getcourses(courses):
        return courses
    
    def getgpa(gpa):
        return gpa
    