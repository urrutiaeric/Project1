class Staff:
    def __init__(self, staff_id, staff_name, staff_role):

        self.staff_id = staff_id
        self.staff_name = staff_name
        self.staff_role = staff_role

    def get_staff_id(staff_id):
        return staff_id
    
    def get_staff_name(staff_name):
        return staff_name
    
    def get_staff_role(staff_role):
        return staff_role
    