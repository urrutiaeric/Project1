class Grade:
    def __init__(self, student_id, course_id, grade):
        self.student_id = student_id
        self.course_id = course_id
        self.grade = grade
        
    def get_student_id(student_id):
        return student_id
    
    def get_course_id(course_id):
        return course_id
    
    def get_grade(grade):
        return grade